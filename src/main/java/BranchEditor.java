    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        if(bran.list.size() > 0)
            bran.list.remove();
        else
            JOptionPane.showMessageDialog(rootPane, "No Customers Present");
        if(bran.list.size() > 0)
        {
            mod.clear();
            pres();
        }
        else 
        {
            mod.clear();
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String s = lst.getSelectedValuesList().toString();
        int i = lst.getSelectedIndex();
        if(i == -1)
        {
            JOptionPane.showMessageDialog(rootPane, "No Customer Selected");
        }
        else
        {
            int x = bran.list.size();
            PriorityQueue dup = new PriorityQueue(bran.list);
            Customer[] tem = new Customer[x];
            for(int k = 0; k < tem.length; k++)
            {
                tem[k] = (Customer) dup.remove();
            }        
            AccessCustomer ac = new AccessCustomer(tem[i]);
            ac.setVisible(true);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

