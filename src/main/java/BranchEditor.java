    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String s = lst.getSelectedValuesList().toString();
        int i = lst.getSelectedIndex();
        if(i == -1)
        {
            JOptionPane.showMessageDialog(rootPane, "No Customer Selected");
        }
        else
        {
            c.remove(i);
            ArrayList<Customer> del = new ArrayList();
            PriorityQueue<Customer> dup = new PriorityQueue(bran.list);
            for(int k = 0; k < dup.size(); k++)
            {
                del.add(dup.remove());
            }
            del.remove(i);
            for(int o = 0; o < del.size(); o++)
            {
                dup.add(del.get(o));
            }

            bran.list = dup;
            mod.clear();
            pres();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

