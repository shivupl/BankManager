    public DefaultListModel mod = new DefaultListModel();
    
    public void pres()
    {
        PriorityQueue<Customer> temp = new PriorityQueue(bran.list);
        Iterator it = temp.iterator();
        while(it.hasNext())
        {
            mod.addElement(Long.toString(temp.poll().number));
        }
    }

